namespace Mirage.TextKit
{
    /// <summary>
    /// Represents a horizontal text alignment.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Align text to the left of the container.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Align text to the center of the container.
        /// </summary>
        Center = 1,

        /// <summary>
        /// Align text to the right of the container.
        /// </summary>
        Right = 2
    }
}
